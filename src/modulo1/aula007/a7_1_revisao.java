package modulo1.aula007;

public class a7_1_revisao {
    public static void main(String[] args) {

        int contador = 0;
        while (contador < 5) {
            System.out.println(contador);
            contador++;
        }
        System.out.printf("O valor final: %d", contador);


        int numero = 0;
        do {
            System.out.println(numero);
            numero ++;
        } while (numero <= 10);
    }
}
